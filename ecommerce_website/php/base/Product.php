<?php

class Product
{
    private $id;
    private $name;
    private $description;
    private $price;
    private $quantity;

    public function __construct()
    {
        $this->id = $id;
        $this->name = $name;
        $this->description = $description;
        $this->price = $price;
        $this->quantity = $quantity;
    }
}
